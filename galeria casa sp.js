  (function () {
    var reduceMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

    // Revelação ao rolar
    var revealItems = document.querySelectorAll('.gallery .reveal');
    if (revealItems.length) {
      if (reduceMotion || !('IntersectionObserver' in window)) {
        revealItems.forEach(function (el) { el.classList.add('is-visible'); });
      } else {
        var observer = new IntersectionObserver(function (entries) {
          entries.forEach(function (entry) {
            if (entry.isIntersecting) {
              entry.target.classList.add('is-visible');
              observer.unobserve(entry.target);
            }
          });
        }, { threshold: 0.1 });
        revealItems.forEach(function (el) { observer.observe(el); });
      }
    }

    // Aceno único no carrossel do celular, pra sinalizar que dá pra arrastar
    var galleryGrid = document.querySelector('.gallery-grid');
    if (galleryGrid && !reduceMotion) {
      var nudged = false;
      var nudgeObserver = new IntersectionObserver(function (entries) {
        entries.forEach(function (entry) {
          if (entry.isIntersecting && !nudged) {
            nudged = true;
            window.setTimeout(function () {
              galleryGrid.scrollTo({ left: 54, behavior: 'smooth' });
              window.setTimeout(function () {
                galleryGrid.scrollTo({ left: 0, behavior: 'smooth' });
              }, 550);
            }, 500);
          }
        });
      }, { threshold: 0.3 });
      nudgeObserver.observe(galleryGrid);
    }

    // Controles do carrossel (setas + bolinhas) — em todas as telas
    var gcDots = document.getElementById('gcDots');
    var gcPrev = document.getElementById('gcPrev');
    var gcNext = document.getElementById('gcNext');
    if (galleryGrid && gcDots && gcPrev && gcNext) {
      var cards = Array.prototype.slice.call(galleryGrid.querySelectorAll('.gallery-item'));

      function visibleCount() {
        // quantas fotos cabem por "página" (largura do trilho / largura de um card)
        if (!cards.length) return 1;
        var cardW = cards[0].getBoundingClientRect().width + parseFloat(getComputedStyle(galleryGrid).columnGap || 0);
        return Math.max(1, Math.round(galleryGrid.clientWidth / cardW));
      }

      var dots = [];
      function buildDots() {
        gcDots.innerHTML = '';
        dots = [];
        var per = visibleCount();
        var pages = Math.max(1, Math.ceil(cards.length / per));
        for (var p = 0; p < pages; p++) {
          (function (page) {
            var dot = document.createElement('button');
            dot.type = 'button';
            dot.className = 'gc-dot';
            dot.setAttribute('aria-label', 'Ir para grupo ' + (page + 1));
            dot.addEventListener('click', function () {
              var target = cards[Math.min(page * per, cards.length - 1)];
              galleryGrid.scrollTo({ left: target.offsetLeft, behavior: 'smooth' });
            });
            gcDots.appendChild(dot);
            dots.push(dot);
          })(p);
        }
      }

      function currentPage() {
        var per = visibleCount();
        var idx = Math.round(galleryGrid.scrollLeft / (cards[0].getBoundingClientRect().width + parseFloat(getComputedStyle(galleryGrid).columnGap || 0)));
        return Math.min(Math.floor(idx / per), dots.length - 1);
      }

      function sync() {
        var page = currentPage();
        dots.forEach(function (d, i) { d.classList.toggle('is-active', i === page); });
        var atStart = galleryGrid.scrollLeft <= 2;
        var atEnd = galleryGrid.scrollLeft + galleryGrid.clientWidth >= galleryGrid.scrollWidth - 2;
        gcPrev.disabled = atStart;
        gcNext.disabled = atEnd;
      }

      function page(dir) {
        var per = visibleCount();
        var step = (cards[0].getBoundingClientRect().width + parseFloat(getComputedStyle(galleryGrid).columnGap || 0)) * per;
        galleryGrid.scrollBy({ left: dir * step, behavior: 'smooth' });
      }

      gcPrev.addEventListener('click', function () { page(-1); });
      gcNext.addEventListener('click', function () { page(1); });

      var scrollTimer = null;
      galleryGrid.addEventListener('scroll', function () {
        if (scrollTimer) window.cancelAnimationFrame(scrollTimer);
        scrollTimer = window.requestAnimationFrame(sync);
      });

      var resizeTimer = null;
      window.addEventListener('resize', function () {
        if (resizeTimer) window.clearTimeout(resizeTimer);
        resizeTimer = window.setTimeout(function () { buildDots(); sync(); }, 150);
      });

      buildDots();
      sync();

      // Botão "Ver todas as fotos" — alterna carrossel <-> grade (desktop)
      var gcViewAll = document.getElementById('gcViewAll');
      var gallerySection = document.querySelector('.gallery');
      if (gcViewAll && gallerySection) {
        var vaText = gcViewAll.querySelector('.gc-va-text');
        gcViewAll.addEventListener('click', function () {
          var expanded = gallerySection.classList.toggle('is-expanded');
          gcViewAll.setAttribute('aria-pressed', expanded ? 'true' : 'false');
          vaText.textContent = expanded ? 'Ver como carrossel' : 'Ver todas as fotos';
          if (!expanded) {
            galleryGrid.scrollTo({ left: 0 });
            buildDots();
            sync();
            gallerySection.scrollIntoView({ behavior: 'smooth', block: 'start' });
          }
        });
      }
    }

    // Lightbox
    var items = Array.prototype.slice.call(document.querySelectorAll('.gallery-item'));
    var lightbox = document.getElementById('lightbox');

    // Reposiciona o lightbox pra cobrir a TELA real, não a seção.
    // Em construtores como o Hostinger, seções ficam dentro de containers com "transform",
    // o que faz "position: fixed" se ancorar nesse container em vez da janela. Para
    // contornar, medimos onde o lightbox caiu (getBoundingClientRect) e o empurramos de
    // volta pro canto 0,0 da tela com um deslocamento compensatório.
    document.body.appendChild(lightbox);

    function anchorToViewport() {
      // zera primeiro para medir o deslocamento causado pelo ancestral transformado
      lightbox.style.setProperty('left', '0px', 'important');
      lightbox.style.setProperty('top', '0px', 'important');
      var rect = lightbox.getBoundingClientRect();
      lightbox.style.setProperty('left', (-rect.left) + 'px', 'important');
      lightbox.style.setProperty('top', (-rect.top) + 'px', 'important');
      lightbox.style.setProperty('width', window.innerWidth + 'px', 'important');
      lightbox.style.setProperty('height', window.innerHeight + 'px', 'important');
    }

    var lbImg = document.getElementById('lbImg');
    var lbCounter = document.getElementById('lbCounter');
    var lbClose = document.getElementById('lbClose');
    var lbPrev = document.getElementById('lbPrev');
    var lbNext = document.getElementById('lbNext');
    var lbSpinner = document.getElementById('lbSpinner');
    var lbThumbs = document.getElementById('lbThumbs');
    var currentIndex = 0;
    var lastFocused = null;

    // Monta as miniaturas uma vez
    var thumbButtons = [];
    items.forEach(function (item, i) {
      var src = item.querySelector('img').src;
      var btn = document.createElement('button');
      btn.type = 'button';
      btn.className = 'lb-thumb';
      btn.setAttribute('aria-label', 'Ir para imagem ' + (i + 1));
      var thumbImg = document.createElement('img');
      thumbImg.src = src;
      thumbImg.alt = '';
      thumbImg.loading = 'lazy';
      btn.appendChild(thumbImg);
      btn.addEventListener('click', function () { show(i); });
      lbThumbs.appendChild(btn);
      thumbButtons.push(btn);
    });

    function show(index) {
      currentIndex = (index + items.length) % items.length;
      var img = items[currentIndex].querySelector('img');

      lbImg.classList.add('is-loading');
      lbSpinner.classList.add('is-active');

      var loader = new Image();
      loader.onload = function () {
        lbImg.src = img.src;
        lbImg.alt = img.alt;
        lbImg.classList.remove('is-loading');
        lbSpinner.classList.remove('is-active');
      };
      loader.src = img.src;
      // se já estiver em cache, onload pode não disparar a tempo
      if (loader.complete) { loader.onload(); }

      lbCounter.textContent = (currentIndex + 1) + ' / ' + items.length;

      thumbButtons.forEach(function (b, i) {
        b.classList.toggle('is-active', i === currentIndex);
      });
      var activeThumb = thumbButtons[currentIndex];
      if (activeThumb && activeThumb.scrollIntoView) {
        activeThumb.scrollIntoView({ inline: 'center', block: 'nearest' });
      }
    }

    function openLightbox(index) {
      lastFocused = document.activeElement;
      show(index);
      lightbox.hidden = false;
      anchorToViewport();
      lbClose.focus();
      document.body.style.overflow = 'hidden';
    }

    function closeLightbox() {
      lightbox.hidden = true;
      document.body.style.overflow = '';
      if (lastFocused) lastFocused.focus();
    }

    window.addEventListener('resize', function () {
      if (!lightbox.hidden) anchorToViewport();
    });
    window.addEventListener('scroll', function () {
      if (!lightbox.hidden) anchorToViewport();
    }, true);

    items.forEach(function (item, i) {
      item.addEventListener('click', function () { openLightbox(i); });
    });

    lbClose.addEventListener('click', closeLightbox);
    lbPrev.addEventListener('click', function () { show(currentIndex - 1); });
    lbNext.addEventListener('click', function () { show(currentIndex + 1); });

    lightbox.addEventListener('click', function (e) {
      if (e.target === lightbox) closeLightbox();
    });

    document.addEventListener('keydown', function (e) {
      if (lightbox.hidden) return;
      if (e.key === 'Escape') closeLightbox();
      if (e.key === 'ArrowLeft') show(currentIndex - 1);
      if (e.key === 'ArrowRight') show(currentIndex + 1);
      if (e.key === 'Tab') {
        var focusable = [lbClose, lbPrev, lbNext];
        var idx = focusable.indexOf(document.activeElement);
        e.preventDefault();
        var nextIdx = e.shiftKey ? idx - 1 : idx + 1;
        focusable[(nextIdx + focusable.length) % focusable.length].focus();
      }
    });
  })();
